export interface OilData {
  id: string;
  name: string;
  category: 'oil' | 'butter' | 'fat' | 'wax';
  /** NaOH SAP value (grams NaOH per gram of oil) */
  sapNaOH: number;
  /** KOH SAP value (mg KOH per gram of oil) */
  sapKOH: number;
  /** Iodine value */
  iodine: number;
  /** INS value (KOH SAP - Iodine) */
  ins: number;
  /** Fatty acid profile (percentages, should sum to ~100) */
  fattyAcids: {
    lauric: number;
    myristic: number;
    palmitic: number;
    stearic: number;
    oleic: number;
    linoleic: number;
    linolenic: number;
    ricinoleic: number;
  };
  /** Cost tier for recipe generation */
  costTier: 'budget' | 'mid' | 'premium';
  /** Common usage notes */
  notes: string;
  /** Maximum recommended percentage in recipe */
  maxPercent: number;
}

